package com.cxc.firstproject.view.refresh;

/**
 * 圆
 */
public class Circle
{
	private float x;

	private float y;

	/**
	 * 半径
	 */
	private float radius;

	private int color;

	public float getX()
	{
		return x;
	}

	public void setX(float x)
	{
		this.x = x;
	}

	public float getY()
	{
		return y;
	}

	public void setY(float y)
	{
		this.y = y;
	}

	public float getRadius()
	{
		return radius;
	}

	public void setRadius(float radius)
	{
		this.radius = radius;
	}

	public int getColor()
	{
		return color;
	}

	public void setColor(int color)
	{
		this.color = color;
	}

}
